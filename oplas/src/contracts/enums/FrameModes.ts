/**
 * Frame Modes
 * 
 * Coordinate frame declarations for programs.
 * RELATIVE is default; ABSOLUTE requires justification + translation test.
 * 
 * Version: 1.0.0
 */

export enum FrameMode {
  /** Relative coordinates (default) */
  RELATIVE = 'RELATIVE',
  /** Absolute coordinates (requires justification) */
  ABSOLUTE = 'ABSOLUTE'
}























